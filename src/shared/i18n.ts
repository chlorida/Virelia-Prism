import type { UiLanguagePreference } from './types';

export type UiLocale = 'en' | 'ru';

export function detectLocaleFromTag(localeTag: string): UiLocale {
  const normalized = localeTag.trim().toLowerCase().replace(/_/g, '-');
  const primary = normalized.split('-')[0] ?? normalized;
  if (primary === 'ru' || primary === 'uk' || primary === 'be') return 'ru';
  return 'en';
}

export function resolveUiLocale(preference: UiLanguagePreference, systemLocaleTag: string): UiLocale {
  if (preference === 'en' || preference === 'ru') return preference;
  return detectLocaleFromTag(systemLocaleTag);
}

